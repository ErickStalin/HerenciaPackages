package geometria2D;
public class cuadrado {
    double lCua, areaCua, perimetroCua;

    public cuadrado(double lCua, double areaCua, double perimetroCua) {
        this.lCua = lCua;
        this.areaCua = areaCua;
        this.perimetroCua = perimetroCua;
    }

    public double getlCua() {
        return lCua;
    }

    public void setlCua(double lCua) {
        this.lCua = lCua;
    }

    public double getAreaCua() {
        return areaCua;
    }

    public void setAreaCua(double areaCua) {
        this.areaCua = areaCua;
    }

    public double getPerimetroCua() {
        return perimetroCua;
    }

    public void setPerimetroCua(double perimetroCua) {
        this.perimetroCua = perimetroCua;
    }

}
